import math

# 1. Syntax : sqrt(number)
print(math.sqrt(5))

# 2. Syntax : ceil(number)
print(math.ceil(2.6))

# 3. Syntax : floor(number)
print(math.floor(2.4))

# 4. Syntax : fabs(number)
print(math.fabs(-98))

# 5. Syntax : radian(x)- converts angle x from degree to radians.
print(math.radians(90))

# 6. Syntax : degree(x)- converts angle x from radians to degree.
print(math.degrees(6))

# 7. Syntax : Pow(x,y)
print(math.pow(12,2))